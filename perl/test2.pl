#\a - used to make a notification sound (alert bell)
#\t - horizontal tab (1tab=5space)
#\u - to uppercase the first character
#\l - to lowercase the first character
#\U - converts the full sentence to uppercase
#\L - converts the full sentence to lowercase
#\Q - used for separator (after every space,elements are separated by "\")

print "I am arnab \a \n";
print "I am arnab \t durgapur \n";
print "I am arnab \udurgapur \n";
print "I am arnab \lDurgapur \n";
print "I am arnab \Udurgapur \n";
print "I am arnab \LDURGAPUR \n";
print "\Q I am Arnab \n";
print "\Q!@#^&*-+%\n";